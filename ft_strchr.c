#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	size_t i;

	i = 0;
	while (s[i] && s[i] != c)
		i++;
	if (s[i] == c || c == '\0')
		return ((char *)&s[i]);
	return (NULL);
}
